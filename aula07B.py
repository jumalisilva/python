# Importando bibliotecas

# Bibliotecas são blocos de códigosprontos para otimizar e potencializar nossa codificação

import math # importando a biblioteca math para o código

# 1) math.ceil() serve para arredondar valores para cima
a = math.ceil(8.5)
print("Arredondando:", a) # retorna 9

# 2) math.sqrt() serve para achar a raíz quadrada de um número
b = math.sqrt(49)
print("Raíz quadrada:", b) # retorna 7